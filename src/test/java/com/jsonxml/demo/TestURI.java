package com.jsonxml.demo;

public class TestURI {

	String XmlApi, JsonApi;

	public TestURI(String xmlApi, String jsonApi) {
		super();
		XmlApi = xmlApi;
		JsonApi = jsonApi;
	}

	public String getXmlApi() {
		return XmlApi;
	}

	public void setXmlApi(String xmlApi) {
		XmlApi = xmlApi;
	}

	public String getJsonApi() {
		return JsonApi;
	}

	public void setJsonApi(String jsonApi) {
		JsonApi = jsonApi;
	}
}
